# math_module_example.py
import math

def study_math_module():
    print("Math Module:")
    # Calculate square root
    sqrt_val = math.sqrt(16)
    print(f"Square Root of 16: {sqrt_val}")

    # Calculate factorial
    factorial_val = math.factorial(5)
    print(f"Factorial of 5: {factorial_val}")

    # Calculate cosine
    cosine_val = math.cos(math.pi / 4)
    print(f"Cosine of 45 degrees: {cosine_val}")

if __name__ == "__main__":
    study_math_module()
